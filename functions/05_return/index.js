/**
 * RETURN keyword
 */

//The return keyword in JavaScript is used within a function to perform two primary actions

/*
 *1:- Terminate Function Execution
 */

//When the return statement is encountered inside a function,
//the function's execution is immediately stops.
//Any code written after the return statement within the same function will not be executed.

/*
 *2:- Return A value
 */

//The return keyword can be followed by an expression or a value.
//This value is sent back to the caller of the function
//This returned value can be of any datatype.
//Including Primitive or Non-Primitive datatypes.
//If no value is explicitly specified after return, the function
//implicitly returns undefined.